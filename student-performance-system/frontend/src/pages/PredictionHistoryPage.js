import React, { useEffect, useState } from 'react';
import { useAuth } from '../context/AuthContext';
import Alert from '../components/common/Alert';
import EmptyState from '../components/common/EmptyState';
import Spinner from '../components/common/Spinner';
import { getApiErrorMessage } from '../services/apiClient';
import predictionService from '../services/predictionService';
import { isAdminLikeRole } from '../utils/roles';

const formatDate = (value) =>
  new Intl.DateTimeFormat('en-IN', {
    day: '2-digit',
    month: 'short',
    year: 'numeric',
    hour: '2-digit',
    minute: '2-digit',
  }).format(new Date(value));

const PredictionHistoryPage = () => {
  const { user } = useAuth();
  const [predictions, setPredictions] = useState([]);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState('');

  const loadHistory = async () => {
    setLoading(true);
    setError('');

    try {
      const response = await predictionService.getHistory();
      setPredictions(response);
    } catch (error) {
      setError(getApiErrorMessage(error, 'Unable to load prediction history.'));
    } finally {
      setLoading(false);
    }
  };

  useEffect(() => {
    loadHistory();
  }, []);

  return (
    <div className="content-stack">
      <section className="page-header">
        <div>
          <p className="eyebrow">Prediction Audit Trail</p>
          <h1>Prediction History</h1>
          <p className="page-copy">
            {isAdminLikeRole(user?.role)
              ? 'Review every prediction generated through the admin and user portal, including who created it and which student record was used.'
              : 'Review the prediction results saved under your account so you can revisit older model outputs quickly.'}
          </p>
        </div>
      </section>

      <section className="panel">
        <div className="section-header">
          <div>
            <p className="eyebrow">Saved Results</p>
            <h2>Prediction Log</h2>
          </div>
          <button className="button-secondary button-compact" onClick={loadHistory} type="button">
            Refresh
          </button>
        </div>

        <Alert tone="error">{error}</Alert>

        {loading ? (
          <Spinner label="Loading prediction history..." />
        ) : predictions.length === 0 ? (
          <EmptyState
            title="History is empty"
            description="Run a prediction from the portal and it will appear here automatically."
          />
        ) : (
          <div className="table-shell">
            <table className="data-table">
              <thead>
                <tr>
                  <th>Student</th>
                  <th>Predicted Score</th>
                  <th>Status</th>
                  <th>Risk</th>
                  {isAdminLikeRole(user?.role) && <th>Generated By</th>}
                  <th>Date</th>
                </tr>
              </thead>
              <tbody>
                {predictions.map((prediction) => (
                  <tr key={prediction._id}>
                    <td>
                      {prediction.student?.studentName || prediction.studentName || 'Manual prediction'}
                      <br />
                      <span className="field-hint">
                        {prediction.student?.registerNo || prediction.registerNo || 'No register number'}
                      </span>
                    </td>
                    <td>{Number(prediction.predictedScore).toFixed(1)}</td>
                    <td>
                      <span className={`badge ${prediction.pass ? 'badge-success' : 'badge-danger'}`}>
                        {prediction.pass ? 'Pass' : 'Needs Attention'}
                      </span>
                    </td>
                    <td>{prediction.riskLevel}</td>
                    {isAdminLikeRole(user?.role) && (
                      <td>{prediction.generatedBy?.name || prediction.generatedBy?.email || 'Portal user'}</td>
                    )}
                    <td>{formatDate(prediction.createdAt)}</td>
                  </tr>
                ))}
              </tbody>
            </table>
          </div>
        )}
      </section>
    </div>
  );
};

export default PredictionHistoryPage;
